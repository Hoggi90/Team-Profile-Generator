// importing the employee constructor class
import Employee from "./Employee.js";

// constructor class that extends from employee
export default class Engineer extends Employee {
    constructor(name, id, email, github) {
        super(name, id, email);
        this.github = github;
    }

    getGithub() {
        return this.github;
    }

    getRole() {
        return "Engineer";
    }

};